def test_orchestrator_saves_report_and_artifacts_on_success(monkeypatch, tmp_path):
    _patch_common(monkeypatch, tmp_path)

    orchestrator = Orchestrator()
    orchestrator.aether = DummyAether(action="run_nmap")
    orchestrator.scribe = DummyScribe(report_text="# Success Report\n\nAll good.")
    orchestrator.watcher = DummyWatcher()

    saved_memory: list[tuple[str, dict]] = []
    saved_findings: list[dict] = []
    saved_tool_runs: list[dict] = []

    monkeypatch.setattr(
        orchestrator_module,
        "validate_single_target",
        lambda target: ScopeCheckResult(True, "test.local", "Valid hostname target."),
    )
    monkeypatch.setattr(
        orchestrator_module,
        "parse_nmap_output",
        lambda output: {
            "host": "test.local",
            "host_status": "up",
            "filtered_summary": None,
            "closed_summary": None,
            "open_ports": [80],
            "services": ["http"],
            "port_details": [
                {
                    "port": 80,
                    "protocol": "tcp",
                    "state": "open",
                    "service": "http",
                    "version": "nginx 1.24.0",
                }
            ],
            "os_hint": "Linux",
            "scan_protocols_seen": ["tcp"],
            "all_port_states": {"open": 1},
            "evidence_count": 1,
        },
    )

    def fake_save_findings(session_id, parsed_output, task_id=None, tool_run_id=None):
        saved_findings.append(
            {
                "session_id": session_id,
                "parsed_output": parsed_output,
                "task_id": task_id,
                "tool_run_id": tool_run_id,
            }
        )

    def fake_save_tool_run(**kwargs):
        saved_tool_runs.append(kwargs)
        return 99

    monkeypatch.setattr(
        orchestrator_module,
        "save_session_memory",
        lambda session_id, data: saved_memory.append((session_id, data)),
    )
    monkeypatch.setattr(orchestrator_module, "save_tool_run", fake_save_tool_run)
    monkeypatch.setattr(orchestrator_module, "save_findings", fake_save_findings)

    tool = DummyTool(
        {
            "tool": "nmap",
            "command": "nmap -Pn -sV test.local",
            "stdout": "Nmap scan report for test.local\nHost is up.\n80/tcp open http nginx 1.24.0\n",
            "stderr": "",
            "returncode": 0,
        }
    )
    monkeypatch.setitem(orchestrator_module.TOOL_REGISTRY, "run_nmap", _tool_entry(tool))

    session_id = "session-success"
    task = Task(
        session_id=session_id,
        objective="scan test.local",
        target="test.local",
        approved=True,
    )
    result = orchestrator.run_task(task)

    session_path = tmp_path / session_id

    assert saved_findings[0]["session_id"] == session_id
    assert saved_findings[0]["task_id"] == task.id
    assert saved_findings[0]["tool_run_id"] == 99
    assert result["status"] == "completed"
    assert result["tool_card"]["key"] == "run_nmap"
    assert result["tool_card"]["approval_class"] == "explicit_approval"
    assert result["parsed_result"]["host"] == "test.local"
    assert result["next_steps"] == ["run_http_probe"]

    assert (session_path / "nmap_stdout.txt").exists()
    assert (session_path / "nmap_stderr.txt").exists()
    assert (session_path / "report.md").exists()
    assert (session_path / "events.jsonl").exists()

    assert (session_path / "nmap_stdout.txt").read_text(encoding="utf-8") == tool.result["stdout"]
    assert (session_path / "report.md").read_text(encoding="utf-8") == "# Success Report\n\nAll good."

    assert tool.called_with == "test.local"
    assert len(saved_tool_runs) == 1
    assert len(saved_findings) == 1
    assert len(saved_memory) == 1

def test_orchestrator_returns_tool_error_on_nonzero_returncode(monkeypatch, tmp_path):
    _patch_common(monkeypatch, tmp_path)

    orchestrator = Orchestrator()
    orchestrator.aether = DummyAether(action="run_nmap")

    monkeypatch.setattr(
        orchestrator_module,
        "validate_single_target",
        lambda target: ScopeCheckResult(True, "10.0.0.9", "Valid single IP target."),
    )

    tool = DummyTool(
        {
            "tool": "nmap",
            "command": "nmap -Pn -sV 10.0.0.9",
            "stdout": "",
            "stderr": "nmap execution failed",
            "returncode": 2,
        }
    )
    monkeypatch.setitem(orchestrator_module.TOOL_REGISTRY, "run_nmap", _tool_entry(tool))

    session_id = "session-tool-error"
    result = orchestrator.run_task(
        Task(
            session_id=session_id,
            objective="scan 10.0.0.9",
            target="10.0.0.9",
            approved=True,
        )
    )

    session_path = tmp_path / session_id

    assert result["status"] == "tool_error"
    assert result["message"] == "Tool execution failed."
    assert result["tool_card"]["key"] == "run_nmap"
    assert result["raw_result"]["returncode"] == 2

    assert (session_path / "nmap_stdout.txt").exists()
    assert (session_path / "nmap_stderr.txt").exists()
    assert (session_path / "events.jsonl").exists()
    assert not (session_path / "report.md").exists()